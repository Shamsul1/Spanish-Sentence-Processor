package Model;

public class WordData {

    String infinitive;
    String conjugation;
    String person;
    String englishSentence;
    String spanishSentence;

    public String getInfinitive() {
        return infinitive;
    }

    public void setInfinitive(String infinitive) {
        this.infinitive = infinitive;
    }

    public String getConjugation() {
        return conjugation;
    }

    public void setConjugation(String conjugation) {
        this.conjugation = conjugation;
    }

    public String getPerson() {
        return person;
    }

    public void setPerson(String person) {
        this.person = person;
    }

    public String getEnglishSentence() {
        return englishSentence;
    }

    public void setEnglishSentence(String englishSentence) {
        this.englishSentence = englishSentence;
    }

    public String getSpanishSentence() {
        return spanishSentence;
    }

    public void setSpanishSentence(String spanishSentence) {
        this.spanishSentence = spanishSentence;
    }
}
